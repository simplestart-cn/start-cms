<template>
  <el-table :data="formatData" :row-style="showRow" row-key="id" v-bind="$attrs" @selection-change="handleSelectionChange" @row-click="handleRowClick">
    <el-table-column type="selection" width="50px" align="center" />
    <el-table-column v-if="columns.length===0" width="150">
      <template slot-scope="scope">
        {{ scope.$index }}
      </template>
    </el-table-column>
    <el-table-column v-for="column in columns" v-else :key="column.value" :label="column.text" :width="column.width">
      <template slot-scope="scope">
        {{ scope.row[column.value] }}
      </template>
    </el-table-column>
    <slot />
  </el-table>
</template>

<script>
export default {
  name: 'TreeTable',
  props: {
    /* eslint-disable */
    data: {
      type: [Array, Object],
      required: true
    },
    columns: {
      type: Array,
      default: () => []
    },
    evalFunc: Function,
    evalArgs: Array,
    expandAll: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    // 格式化数据源
    formatData: function() {
      let tmp
      if (!Array.isArray(this.data)) {
        tmp = [this.data]
      } else {
        tmp = this.data
      }
      return tmp
    }
  },
  methods: {
    showRow: function(row) {
      const show = (row.row.parent ? (row.row.parent._expanded && row.row.parent._show) : true)
      row.row._show = show
      return show ? 'animation:treeTableShow 1s;-webkit-animation:treeTableShow 1s;' : 'display:none;'
    },
		handleSelectionChange(val) {
			this.$emit('selection-change',val)
		},
    handleRowClick(val) {
      this.$emit('row-click', val)
    }
  }
}
</script>
